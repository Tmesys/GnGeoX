#ifndef _TYPE_H
#define _TYPE_H

/* Basic data types */
typedef enum {
        SDL_FALSE = 0,
        SDL_TRUE  = 1
} SDL_bool;


typedef unsigned char   Uint8;
typedef signed char     Sint8;
typedef unsigned short  Uint16;
typedef signed short    Sint16;
typedef unsigned int    Uint32;
typedef signed int      Sint32;


#endif
